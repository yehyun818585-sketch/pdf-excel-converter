from reportlab.lib.pagesizes import A4
from reportlab.lib.units import mm
from reportlab.pdfbase import pdfmetrics
from reportlab.pdfbase.ttfonts import TTFont
from reportlab.pdfgen import canvas

pdfmetrics.registerFont(TTFont('KR', '/usr/share/fonts/truetype/wqy/wqy-zenhei.ttc'))
FONT = 'KR'

def draw_lines(c, lines, start_y=277*mm, lx=22*mm):
    y = start_y
    for item in lines:
        if item is None:
            y -= 5*mm
            continue
        text, size, align = item
        c.setFont(FONT, size)
        if align == 'c':
            c.drawCentredString(105*mm, y, text)
        elif align == 'r':
            c.drawRightString(188*mm, y, text)
        else:
            c.drawString(lx, y, text)
        y -= (size + 3.2)
    return y

